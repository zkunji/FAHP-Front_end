                                                            ****大学内部控制有效性评价报告
一、内部控制有效性评价结果分析
通过上述评价结果可见，治理层面的指标要素组织架构、决策机制、执行机制、监督机制、协调机制、评价机制的模糊综合评价结果依次为：“良好”、“良好”、“良好”、“良好”、“良好”、“优秀”；业务层面的指标要素预算控制、收支控制、采购控制、资产控制、工程项目控制、会计控制及合同控制模糊综合评价结果依次为：“警戒”、“良好”、“警戒”、“警戒”、“良好”、“警戒”、“警戒”。而内部控制整体有效性评价结果为“良好”，反映了***大学的内部控制在治理层面的情况相对好，但在业务层面有关预算控制、采购控制、资产控制、会计控制及合同控制方面有待进一步改进，决策机制、执行机制、协调机制还可以进一步优化。
治理层面权重系数最高的两个指标是决策机制和协调机制，虽然这两个指标的模糊综合评价结果都为“良好”，但在评价过程中其“危险”程度分别达到10.8%和5.6%，决策机制中主要是风险评估做得不完全到位，风险评估包含了风险识别、风险分析、风险应对、风险监控等诸多方面，需要专业的风险管理人员构建科学、规范、合理并符合学校实际业务情况特殊性的风险管理框架。执行机制的内控实施责任制和权责分工明确两个指标评价值不高。通过对治理层面指标评价结果的分析可以看出，***大学内部治理治理体系和治理能力仍需进一步提升。
业务层面的控制活动贯穿于具体业务流程之中，直接影响***大学的运行状况，由于控制环节多，投入的资源也相对较多，是***大学管理层应重点关注和精细管理的方面。业务层面权重系数最高的指标是预算控制、工程项目控制和合同控制，预算控制和合同控制的模糊综合评价结果均为“警戒”，主要是预算编审、决算和考评、合同履行和合同后续管理评价值不高。此外，采购控制中采购方式的确定，资产控制中资产配置、资产更新要引起管理层的高度重视，进一步优化。
通过访谈、查阅制度、穿行测试、检查合同及会计资料，以及抽样测试在内的内部控制测试评价方法，对***大学内部控制存在的问题深入分析，发现存在以下问题：
1、内控建设在内容层面仍需要完善
（1）风险评估工作具有浮于表面，未得到单位重视，也未与单位实际情况有机结合。从学校风险评估的初稿来看，主要是列出了管理中存在的普通性风险清单，并没有根据学校的具体情况来进行进一步分析评价，**年省委巡视组、审计厅审计组、市场监督管理局专项检查组多次对***大学近几年各项工作进行了专项巡视、审计和检查，部分事项延伸到以前年度，根据巡视、审计和专项检查结果来看，在治理层面的党务工作、纪检监督、人事管理，业务层面的项目和资金管理、财务管理等领域存在不少问题，尚未进行一次全面深入的风险评估工作。风险评估报告主要是列举了***大学一般性风险，具有普遍性，未针对***大学的实际情况和具体风险进行评估，风险识别、防控仍停留在面上，不深、不细，动态更新缓慢，将导致目前的制度、流程不能够完全覆盖内控风险。
（2）业务流程可操作性待细化。从内控建设工作的实践来看，流程优化细化是重点，执行落地是难点。一方面从内控手册初稿来看，目前***大学业务流程梳理不够清楚详尽，其可执行性大打折扣。另一方面，由于各职能部门在梳理提交业务流程时，未详细落实到具体的岗位，也未确定每个节点对应的流程控制文档，且未设定流程控制文档的样式，导致内部手册中业务流程的可操作性不够细化。
内控流程未落实到具体岗位，未拟定流程控制文档，导致内控流程的可执性不强，影响到内控管理体系的落地，未来进行信息化还将进一步细化。
（3）内控实施考核制度未配套。***大学的内控建设工作未纳入各单位、各部门及人员的工作任务考核范畴，内部控制评价结果应用（含表扬奖励、处罚处分等）也未得到推行。无配套考核制度，导致内控建设工作落实不到位难以问责追责，导致内控实施难以取得实效。
（4）内部控制管理信息系统功能尚未嵌入单位整体的信息系统中。信息化建设是执行落地的有效手段，目前***大学虽然已有内控手册，但并未嵌入到单位整体的信息系统中，有些基础性工作已经信息化建设比较完备，如财务系统相对而言比较完备，涉及会计核算、预决算、收费、资金支付、财务查询等模块；资产处的资产管理系统采用的是**软件，资产处正在筹划开发或启用合同管理软件系统；学生工作处也启用了学生管理系统，教务处也启用了教务管理系统，但各系统尚处于孤岛运行状态，系统之间资源没有整合，难以保证整个内控体系运转协同，像后勤、投资公司、人事等部门，属于内控的关键领域，信息化建设不完备，无法保证内控体系建设的落实落地，也无法实现“一个治理层面、六个业务层面”的全覆盖。***大学信息管理系统之间没有有机整合，影响整个内控体系运转有机协调。
2、决策机制执行不到位
“三重一大”事项集体决策制度在二级学院和部门执行不力。如某些学院未经党政联席会议集体研究，就擅自采购大型仪器设备的情况比较普遍。
（1）决策过程中风险评估做得不到位。如仪器设备采购缺乏民主决策和科学论证。某些学院大型仪器设备购置没有规划，大型大量设备购置缺少集体研究，缺少民主决策环节，造成资源配置不合理，对内部治理造成不利影响；此外设备购置缺乏科学论证，很多设备从未拆封，造成资源极大浪费。
（2）通过风险识别，发现部分项目存在合同履约风险，包括“未付款、未验收”、“已部分、全部付款，未验收”的采购项目。
3、执行机制有待进一步优化
内部控制制度建设仍然存在不足，需要进一步规范化、体系化。
（1）存在制度之间不衔接、不匹配，各部门相互推诿，加大制度执行难度。如***大学《国有资产管理办法》规定：资产验收必须有纪监处、审计处、财务处、资产处代表，以及相关学科专业技术专家、验收标的物所在部门的主管领导在场，验收意见必须在现场确认签字，不得事后补签名；新购资产验收必须按照验收程序办理。但《固定资产管理办法》却规定：固定资产验收由资产归口管理部门依据合同、招投标文件及有关标准组织实施。
（2）制度建设中存在责任主体指向不明，职能重叠，难以界定具体职责的情况。如《货币资金管理办法》中提到由“学校”审批或审定，但未明确“学校”的具体含义，未明确到岗位，具体明确到人，因此，该制度实际难以执行到位。
（3）制度不够全面、要求不明确，没有做到覆盖业务的全领域，存在内部控制盲区。如《国有资产管理制度》提出“各单位应当在每年年底进行资产自查。”但规定并不详细，未明确执行人、执行对象和执行要求，导致实际操作中任务难以执行到位。《基建工程管理办法》未明确代建项目的管理要求，无概预算变更管理要求。《收入管理办法》规定：“学校事业收入、经营收入和其他收入，依据相关规定或者目标管理协议等，由学校或者取得上述收入的部门、独立核算单位管理。由学校统筹的部分列入学校预算。”导致学校部分收入游离预算之外。
4、预算管理严谨性不够
***大学财务处成立了预结算科，设置了专门的预算会计岗位，对内预算和对外预算的职责安排在两个不同的岗位，对外预算是指按照财政厅要求编制预算报表，严格遵从收付实现制编制预算，符合财政对预算编制报送的时效性要求；对内预算是指结合财政预算指标，按照资金来源（含银行贷款），在校内下属院（系）、单位及各部门分解，部分引进权责发生制编制预算，主要由各院（系）、部门上报预算，财务处负责汇总，统筹考虑预算收入，编制校内预算，经***大学校务会、党委会最终审定。
（1）由于编制口径不完全一致，对内和对外预算编制未做到有机统一，存在两张皮的现象。
（2）未建立完善的预算绩效运行监控机制且监控措施不到位。日常预算绩效监控多集中在资金使用情况方面，对资金绩效和项目目标完成情况等方面关注不足，监控措施不到位，不利于规范预算工作，也不利于学校及时掌握预算项目的完成情况，并据以做出合理调整以达到预期绩效。
（3）预算编制不科学。一是个别项目预算编制无依据，如某收入项目近两年实际无执行却一直被编入预算收入，该项事项未取得主管部门批复，也非创收收入，无编制依据。二是个别项目预算编制不合理，导致预决算差异较大。如“人员经费-综治奖”的预算编制导致人员经费支出超预算。三是其他收入预算未细化，预算构成不明确。如租金收入（房屋、场地、门面）、停车收入、科技查新收入等在校内预算编制时未细化至项目或学院、部门。四是“三公”经费预算编制不明确。校内预算未明确“三公”经费预算的内容与金额。
（4）结余结转资金未按规定及时清理。
（5）存在超预算超范围列支的情况。如其他交通费超预算超范围列支。
（6）预算执行不严格。如业务接待费、会议费超预算率分别达118.99%和188.184%。
（7）收入预算和支出预算的执行率均不高。主要因为收入预算中对于开源性收入和科研收入估计不准确；支出预算主要为专项支出和科研支出预算执行率不高，其中科研支出主要因为收入预算执行率不高导致。
5、采购方式的确定存在风险隐患
（1）部分项目以竞争性磋商形式实施，规避公开招标。一是部分项目以非新改扩工程项目名义实施竞争性磋商，规避公开招标。二是工程类项目以竞争性磋商形式实施，规避公开招标。抽查发现存在将单项工程超过200万元的工程改造项目以竞争性磋商形式委托给施工方，规避公开招标。三是拆分项目、规避公开招标。存在将某项目的室外工程（金额400余万元）从主体工程中拆分出来，再拆分为两个金额分别为230余万元和180余万元的项目以竞争性磋商的方式进行政府采购，规避公开招投标。
（2）个别政府采购事项内部立项论证不够详尽。采购由资产处归口管理，目前采购方式分为外部、内部两种类型，外部的执行政府采购流程，内部执行内部采购立项及审批流程。一是***大学政府采购和政府购买服务预算编制不实，且政府采购表中的预算项目与实际采购项目完全脱节，没有对应关系。二是存在无计划、无预算或超预算采购的情况。
6、资产管理存在风险隐患
实物资产由资产处归口管理，其采购验收、盘点等由资产处负责，资产处根据采购、验收保管情况建立台账，货币资金、往来款项及账面其他资产由财务处归口管理，出纳管钱、会计管账。
（1）履行国有资产管理职责不力，造成固定资产损失八千余万元。通过查阅学校资产盘存报告，了解到学校2019年对固定资产进行了一次全面清查，发现固定资产盘亏（有账无物）八千余万元，造成八千余万元的实际损失。
（2）对校办企业监管不严，造成资产损失。一是校办企业存在违规处置国有资产一千余万元。二是随意调整存货账面金额，将年久毁损、过期存货进行账务调减处理，调减依据仅为《关于处理报废资产的报告》，造成存货差额原因没有及时调查了解。
（3）无形资产对外投资效益欠佳。一是决策不慎，合作方资质不良，经营风险大。二是合作项目进展缓慢，合作公司连年亏损。三是履行合同不到位，合作成立的公司成为“僵尸企业”。
（4）资产管理不规范，造成房屋建筑物等固定资产难以盘点。一是房屋资产卡片与实物存在账实不符的情况。二是大多数房屋无产权证及面积图，导致房屋账证不符。三是未建立资产卡片不利于资产的盘点，也不利于资产的规范管理。
7、建设项目管理有待进一步完善
土木工程建设项目由基建处归口管理，其他建设项目按照职能由各单位开展，项目实施中的采购、付款、合同等业务按照内控流程中相关规定执行，具体的技术方面业务由基建处及其他职能部门按照相关业务制度执行。一是存在基建项目建设超面积、超概算的情况。二是个别建设项目完工后未完成后续工作。如未进行项目后评价，包括项目规划建设目的、执行过程、效益、作用和影响所进行的系统的客观的分析。消防验收不及时，存在安全隐患。
8、合同管理不到位
合同由资产处归口管理，业务处室将签订的合同交资产处存档，资产处根据合同档案建立合同台账，党政办、财务处也留存合同副本，并建立台账。发现存在合同管理不到位的情况。
（1）合同签订、审批环节不完整，未将学校所有的合同文本统一设置编码，建立台账。
（2）合同归口管理不完善，部分项目资料未及时归档，合同档案管理不规范导致合同的履行、变更、追责等出现困难。
（3）存在合同签署主体不合规，合同到期业务仍在延续的情况。
（4）存在租金未按合同足额收取的情况。
二、内部控制有效性改进建议
（一）从治理层面进一步优化内部控制环境
学校治理层应高度重视内部控制工作，进一步完善内部控制规范、行为准则和操作手册等有关控制政策和程序，确定内部控制主体，明确了不同层面控制主体的责任，防范和化解办学风险，节约办学成本，提高资源使用效率。
1、明确内部控制工作实施的相关责任。***大学要进一步强化治理层的内部控制责任，为建立和维护有效的内部控制提供根本保障。内部控制建设工作领导小组负责组织实施，财务部门作为内部控制建设牵头部门，负责组织协调推进内部控制工作。同时，应充分发挥内部审计、纪委监察、政府采购、基建、资产管理、党政办、规划法规处等部门或岗位在内部控制中的作用。
2、确定内部控制建设发展规划。
（1）对学校制度进行统一清理更新，逐步建立和完善全面内部控制体系。经过课题组本次分析发现学校现有制度建设存在不完善、不全面的情况，建议学校统一各部门制度要求，形成一套完整清晰的制度层级体系。在着手制定或更新相关制度时，应考虑是否与各层级制度冲突，并保持同步更新，确保各项制度有序运行。各项业务应有明确的制度规定和权限设置，所有人必须严格遵守和执行，此外，还应制定相应的奖惩规定作为执行保障。根据已完成的主要经济业务内控体系建设，要求各部门认真贯彻实施，并总结内控执行过程中的经验和发现的问题，进一步完善内控手段和流程，并逐步推广到其他业务，最终建立全面的内控体系。
（2）加强内控体系的信息化管理。根据内控体系建设的成果，根据制度流程化、将流程表单化、表单责任明确化的建设方针，拟将各业务流程和表单转换为信息系统控制手段，逐步拓展信息化管理的覆盖面，最终建立全面的内部控制信息化管理体系。
（3）将学校本部的内控经验推广到各二级单位，坚持统一的内控建设思想，结合各二级单位的发展要求和管理实际，不断优化二级单位的内控流程和体系建设。建立全校范围内无死角，阳光透明的内控环境，全方位防范风险发生，支撑学校的全面发展战略。
（4）建立内控优化机制，不断完善内控体系建设
为保障内控体系能够适应不断变化的内外部环境，建议建立内控优化机制。明确内控运行的相关组织机构及具体职能，以及具体的执行标准，包括每年在什么时间开展风险评估、什么时间开展流程更新（或在业务发生变化时进行流程更新）、什么时间开展内控测试与评价及报告，应形成什么样的内控文档等。每年的内控工作应当围绕单位的中长期规划及年度目标进行更新。
（二）重视风险评估，构建全面风险管理体系。
***大学应重视自身风险评估，在日常管理中接受全面风险管理理念，逐步实施全面风险管理，为资产安全完整和有效使用、防范舞弊和预防腐败提供合理保障，并在内部控制体系中增加了财务报告公开制度和风险预警制度，以便从源头上预防和遏制大学内部腐败现象的发生，减少浪费和损失，提高财政资金的使用效益。
1、提高风险应对能力。提高风险应对能力就是要在风险识别和风险分析的基础上提高风险管理解决方案制定的适当性，实施的及时性。***大学应明确风险管理决策机构及其职责，按照风险管理策略，针对各类风险制定具体的解决方案，解决方案应涉及风险发生前后所应采取的具体应对措施及风险管理工具。***大学可通过引进“关键风险指标管理”工具来提高企业风险应对能力，关键风险指标管理既能分析风险成因、将关键成因量化，又能够建立风险预警系统，制定预警信息并采取相应的风险处理措施，同时关键风险指标应在企业总体领导和整体战略指导下分解到企业的各个业务单位和职能部门。
2、加强风险监控能力。***大学应将风险监控作为一种日常管理贯穿于学校运行管理过程中，风险监控的常态化能够合理保证风险评估的有效性。学校应对重要业务（如基建维修业务、资产管理、采购业务等）的立项决策，过程管理，成果验收等实际情况进行持续的风险监控，通过诸如穿行测试等方法对内部控制有效性进行检验，并根据存在的缺陷及时改进。审计部门和纪检监察部门应定期对风险管理工作进行检验，并指导业务单位或项目组进行风险自查，对跨部门的各类项目重点监察，按照风险评估状况，及时调整或改进，并报告主管风险管理工作的高管人员。
（三）设立内控评价考核指标体系，进一步形成全方位内部控制评价体系。
目前，***大学已初步建立了一整套内控体系，但内控体系的实施与有效运行的主体责任在于学校自身，内控建设能够发挥作用取决于单位是否恰当应用和执行这套体系。为进一步体现内控评价机制的监督效果，建议学校在一年一度的内控评价时，设立过程指标与结果指标结合的内控评价考核指标体系。过程指标即对内控工作开展本身的进度、质量、成本等方面进行考核，结果指标即对内控体系及风险管理工作开展后，在未来运营中其发挥作用的大小，是否已显现出了相关效应，比如风险事件的减少、效率的提升、成本费用的控制、利润的提高等进行指标设定。一方面做到内控评价公平有据，另外一方面，通过对各部门内控的评级体系，有助于督促各部门更加认真对待内控建设。
